namespace AngryPurpleTiger;

/// <summary>
///     Formatting for name strings.
/// </summary>
public enum StringStyle
{
    /// <summary>
    ///     Capitalizes each word.
    /// </summary>
    Titlecase,

    /// <summary>
    ///     Represents all words as lower case.
    /// </summary>
    Lowercase,

    /// <summary>
    ///     Represents all words as upper case.
    /// </summary>
    Uppercase
}
